package br.com.springboot.model;

public enum Sexo {
    MASCULINO("Masculino"),
    FEMININO("Femino");

    private String descricao;

    Sexo(String descricao) {
        this.descricao = descricao;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
}
